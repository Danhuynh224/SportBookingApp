package com.example.pjbookingsport.model;

public class Province {
    private String id;
    private String name;
    private int type;
    private String typeText;
    private String slug;

    // Getters
    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public int getType() {
        return type;
    }

    public String getTypeText() {
        return typeText;
    }

    public String getSlug() {
        return slug;
    }
}

